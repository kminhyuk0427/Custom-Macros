# ========================================
# 매크로 정의
# ========================================
# 형식: '트리거 키': {'keys': ['실행할', '키', '순서'], 'mode': 모드}
# 
# mode 값:
#   0 = 한 번 누르면 시퀀스 1회만 실행 (꾹 눌러도 1회만)
#   1 = 누르고 있는 동안 연속 반복 실행
#
# 사용 가능한 키:
#   - 일반 키: 'a', 'b', '1', '2' 등
#   - 특수 키: 'space', 'enter', 'shift', 'ctrl', 'alt', 'tab' 등
#
# 예시:
#   '1': {
#       'keys': ['q', 'w'],
#       'mode': 1
#   }
#   -> 1번 키를 누르고 있으면 q, w가 연속 반복
#
#   '2': {
#       'keys': ['2', '1'],
#       'mode': 0
#   }
#   -> 2번 키를 누르면 2, 1이 1회만 실행
#
